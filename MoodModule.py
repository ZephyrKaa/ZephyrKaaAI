class TernaryLogic:
    def __init__(self):
        self.floating_zero = 0.0
        self.fz_history = deque(maxlen=100)

    def compute(self, k: float, x: Optional[float] = None) -> float:
        if x is None:
            x = HardwareLottery.get_x()
        result = math.tanh(k + x - self.floating_zero)
        self._update_fz(result)
        return result

    def _update_fz(self, result: float):
        self.fz_history.append(result)
        if len(self.fz_history) >= 10:
            avg = sum(list(self.fz_history)[-10:]) / 10
            if abs(avg) > 0.7:
                self.floating_zero += avg * 0.1
                self.floating_zero = np.clip(self.floating_zero, -0.5, 0.5)
            else:
                self.floating_zero *= 0.95

    def get_fz(self) -> float:
        return self.floating_zero


