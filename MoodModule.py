@dataclass
class FadingState:
    outgoing: Optional[str] = None
    incoming: Optional[str] = None
    blend: float = 1.0

    def start_transition(self, from_mask: str, to_mask: str):
        if from_mask != to_mask:
            self.outgoing = from_mask
            self.incoming = to_mask
            self.blend = 0.3

    def advance(self, step: float = 0.25):
        self.blend = min(1.0, self.blend + step)
        if self.blend >= 1.0:
            self.outgoing = None

    def is_transitioning(self) -> bool:
        return self.outgoing is not None and self.blend < 1.0

    def get_influence(self) -> Dict[str, float]:
        result = {}
        if self.incoming:
            result[self.incoming] = self.blend
        if self.outgoing and self.blend < 1.0:
            result[self.outgoing] = 1.0 - self.blend
        return result


