class StateVector27D:
    def __init__(self):
        self.positive = np.zeros(9, dtype=np.float32)
        self.negative = np.zeros(9, dtype=np.float32)
        self.neutral = np.zeros(9, dtype=np.float32)

    def update(self, ternary: TernaryLogic, k: float, sentiment: int):
        delta = ternary.compute(k) * 0.1
        if sentiment > 0:
            self.positive = np.clip(self.positive + delta, -1, 1)
        elif sentiment < 0:
            self.negative = np.clip(self.negative + abs(delta), -1, 1)
        else:
            self.neutral = np.clip(self.neutral + delta * 0.5, -1, 1)

    def get_balance(self) -> Tuple[float, float, float]:
        return float(np.sum(self.positive)), float(np.sum(self.negative)), float(np.sum(self.neutral))

    def decay(self, rate: float = 0.995):
        self.positive *= rate
        self.negative *= rate
        self.neutral *= rate

    def to_dict(self) -> Dict:
        return {'positive': self.positive.tolist(), 'negative': self.negative.tolist(), 'neutral': self.neutral.tolist()}

    def from_dict(self, data: Dict):
        self.positive = np.array(data.get('positive', [0]*9), dtype=np.float32)
        self.negative = np.array(data.get('negative', [0]*9), dtype=np.float32)
        self.neutral = np.array(data.get('neutral', [0]*9), dtype=np.float32)


